//*ASSIGNMENT OPERTAOR
let r = 5;

//*ADDITION ASSIGNMENT assigning variable new value
r += 1; //? the same as saying r = r + 1

//* SUBTRACTION ASSIGNMENT
r -= 1 //? the same as writing r = r - 1

//* MULTIPLICATION ASSIGNMENT
r *= 1 //? the same as writing r = r * 1

//* DIVISION ASSIGNMENT
r /= 1 //? the same as r = r/1

//*REMAIDER (modulus) ASSIGNMENT
r %= 1 //? same as r = r % 1..come in handy with fixbuz

//* EXPONENTIAL ASSIGNMENT
r **=1 //? r = r **1 used for loops etc
